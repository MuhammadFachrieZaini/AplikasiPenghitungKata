    private void buttonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCariActionPerformed
        // Mengambil kata yang dicari dari textField
        String kataPencarian = textFieldCari.getText();
        // Mengambil teks dari textArea
        String text = textArea.getText();

        // Membuat pola pencarian untuk kata yang dicari, mengabaikan huruf besar/kecil
        Pattern pola = Pattern.compile("\\b" + kataPencarian + "\\b", Pattern.CASE_INSENSITIVE);
        // Mencocokkan pola dengan teks
        Matcher pencocokan = pola.matcher(text);

        // Menghitung jumlah kecocokan kata yang ditemukan
        int cocok = 0;
        while (pencocokan.find()) {
            cocok++;
        }

        // Menampilkan hasil pencarian pada label
        labelPencarian.setText("Hasil Pencarian : " + cocok + " Kata Ditemukan");
    }//GEN-LAST:event_buttonCariActionPerformed

    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
String text = textArea.getText();
        // Memastikan hitungan terbaru
        hitung(); 
        String counts = "\nJUMLAH HITUNGAN:\n" +
                        "--------------------------\n" +
                        labelKata.getText() + "\n" +
                        labelKarakter.getText() + "\n" +
                        labelKalimat.getText() + "\n" +
                        labelParagraf.getText();
        
        String kontenUntukSimpan = "TEKS ASLI:\n" + text + "\n\n" + counts;

        JFileChooser fileChooser = new JFileChooser();
        // Set filter file ke .txt
        fileChooser.setDialogTitle("Simpan Hasil Hitungan");
        fileChooser.setSelectedFile(new File("hasil_analisis_teks.txt"));
        fileChooser.setFileFilter(new FileNameExtensionFilter("Text Files (*.txt)", "txt"));

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            
            // Pastikan file memiliki ekstensi .txt
            if (!fileToSave.getName().toLowerCase().endsWith(".txt")) {
                fileToSave = new File(fileToSave.getAbsolutePath() + ".txt");
            }

            try {
                // Gunakan OutputStreamWriter dengan encoding UTF-8
                try (OutputStreamWriter writer = new 
                     OutputStreamWriter(new FileOutputStream(fileToSave), "UTF-8")) {
                    writer.write(kontenUntukSimpan);
                }
                JOptionPane.showMessageDialog(this, 
                    "File berhasil disimpan di:\n" + fileToSave.getAbsolutePath(),
                    "Penyimpanan Berhasil", 
                    JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException e) {
                // Tampilkan pesan kesalahan yang lebih detail
                JOptionPane.showMessageDialog(this, 
                    "Gagal menyimpan file!\nDetail Error: " + e.getMessage(), 
                    "Penyimpanan Gagal", 
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed

