    private void hitung() {
    String text = textArea.getText();
        
        // Cek Teks Kosong (setelah trim)
        boolean isEmpty = text.trim().isEmpty();

        // 1. Hitung Karakter
        int jumlahKarakter = text.length();
        labelKarakter.setText("Karakter : " + jumlahKarakter);

        if (isEmpty) {
            // Jika teks kosong/hanya spasi, set semua hitungan ke 0
            labelKata.setText("Kata : 0");
            labelKalimat.setText("Kalimat : 0");
            labelParagraf.setText("Paragraf : 0");
            return;
        }

        // 2. Hitung Kata (Filter Token Kosong)
        // Membagi teks dan memfilter token kosong yang dihasilkan dari spasi ganda
        long jumlahKata = Arrays.stream(text.split("\\s+"))
                .filter(s -> !s.isEmpty())
                .count();
        labelKata.setText("Kata : " + jumlahKata);

        // 3. Hitung Kalimat (Perbaikan Regex untuk Singkatan/Tanda Baca Berurutan)
        // Pola regex: mencari 1 atau lebih spasi setelah tanda baca akhir kalimat (.!?)
        // Lookbehind (?<=[.!?]) memastikan tanda baca tetap ada di token sebelumnya.
        String[] kalimat = text.split("(?<=[.!?])\\s+");
        // Jumlah kalimat adalah jumlah token yang tidak kosong
        long jumlahKalimat = Arrays.stream(kalimat)
                .filter(s -> !s.trim().isEmpty())
                .count();
        labelKalimat.setText("Kalimat : " + jumlahKalimat);

        // 4. Hitung Paragraf (Menghindari Baris Kosong)
        // Membagi berdasarkan satu atau lebih baris baru, lalu trim dan filter baris kosong
        String[] paragraf = text.split("\\n+");
        long jumlahParagraf = Arrays.stream(paragraf)
                .filter(s -> !s.trim().isEmpty())
                .count();
        labelParagraf.setText("Paragraf : " + jumlahParagraf);
    }
    
